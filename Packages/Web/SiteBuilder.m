	$WebSiteSummaryUnits=
		Alternatives@@
			Join[$WebSiteSummaryBaseUnits,$WebSiteSummaryIndependentUnits]


WebSiteTemplateGatherArgs[fileContent_, args_]:=
	With[
		{
			content=
				Replace[fileContent,
					{
							l:{__XMLElement}:>
								StringRiffle[
									WebSiteBuildSafeExportXML/@l,
									"\n"
									],
							x_XMLElement:>
								WebSiteBuildSafeExportXML[x],
							None->
								"",
							e:Except[_String]:>
								(Message[WebSiteBuild::nocnt,Short[e]])
							}
					]
			},
		Merge[
			{
				Replace[
					FirstCase[
						If[StringQ[fileContent],
							ImportString[fileContent,"HTML"],
							fileContent
							],
						t:XMLElement["title",_,_]:>
							System`Convert`XMLDump`getSymbolicXMLPlaintext@
								XMLElement["body",{},t],
						None,
						\[Infinity]
						],
					{
						Except[_String]->
							Nothing,
						s_String:>
							("Title"->s)
						}
					],
				"Summary"->
					With[{
						sl=
							Replace[
								Lookup[args,"SummaryLength"],{
									i_Integer?Positive:>
										{i, "Characters"},
									_[i_Integer?Positive, u:Alternatives@@$WebSiteSummaryBaseUnits]:>
										{i, u},
									_[i_Integer?Positive, u_String?(MatchQ[u, $WebSiteSummaryBaseUnits])]:>
										{i, u},
									_:>
										{1,"Paragraph"}
								}]
						},
						With[
							{
								cont=ImportString[content,"HTML"]
								},
							Replace[sl,{
								{
									i_,
									"Characters"|IndependentUnit["characters"]
									}:>
									Function[
										If[
											StringLength[#]>i,
											StringTake[#,i-3]<>"...",
											#
											]
										]@
										StringReplace[cont,Whitespace->" "],
								{
									i_,
									"Lines"|IndependentUnit["lines"|"lines of code"]
									}:>
									StringRiffle@
										Take[
											StringSplit[cont,"\n"],
											UpTo[i]
											],
								{
									i_,
									IndependentUnit[t_]|t_String
									}:>
									StringRiffle[
										TextCases[cont,
											ToUpperCase[StringTake[#,1]]<>
												StringDrop[#,1]&@t,
											i],
										" "
										]
								}]
							]
						],
				"Date"->
					Replace[
						Lookup[args,"Date",Now],
						s_String:>DateObject[s]
						],
				args,
				"SiteName"->
					Replace[
						Lookup[args,"SiteName",Automatic],
						{
							s_String:>
								URLParse[s,"Path"][[-1]],
							Except[_String]:>
								Replace[
									Lookup[args,"SiteURL"],
									{
										s_String:>
											URLParse[s,"Path"][[-1]],
										Except[_String]:>
											Replace[Lookup[args, "SiteDirectory"],
												Except[_String]:>
													Replace[$WolframID,{
														s_String:>
															StringSplit[s,"@"][[1]],
														_->$UserName
														}]
												]
										}
									]
							}
						],
				"SiteURL"->
					Replace[
						Lookup[args, "SiteURL", Automatic],
						Automatic:>
							With[{
								bit=
									Length@
										Replace[
											Lookup[args,"URL",
												FileNameSplit@
													Lookup[args, "FilePath", "??"]
												],
											s_String:>
												URLParse[s,"Path"]
											]
								},
								Replace[
									bit,
									{
										1->".",
										n_:>
											URLBuild@ConstantArray["..",n-1]
										}
									]
								]
						],
				With[{
					url=Lookup[args, "URL"],
					slug=Lookup[args, "Slug"],
					fp=Lookup[args, "FilePath"]
					},
					Which[
						StringQ@url,
							"URL"->url,
						StringQ@slug,
							"URL"->
								If[StringQ[fp],
									URLBuild@
										Append[Most@FileNameSplit[fp],slug<>".html"],
									slug<>".html"
									],
						True,	
							Nothing
						]
					],
				"Content"->
					content
				},
			Last
			]
		]


