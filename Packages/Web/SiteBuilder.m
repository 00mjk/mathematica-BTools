							]
						],
				(* A function for getting object attributes by file name *)
				"ContentData"->
					Function[
						Lookup[
							Lookup[
								$WebSiteBuildContentDataStack, #,
								Lookup[$WebSiteBuildContentStack, #, <||>]
								],
							"Attributes",
							<||>
							]
						],
