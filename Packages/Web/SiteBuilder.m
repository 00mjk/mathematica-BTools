				"Pages":>
					$WebSiteBuildAggStack["SelectObjects"]["page.html"],
				(* The articles *)
				"Articles":>
					$WebSiteBuildAggStack["SelectObjects"]["article.html"],
				(* The archives *)
				"Archives":>
					Reverse@
						GatherBy[
							Values@
								$WebSiteBuildContentStack[[All,"Attributes"]],
							#["Date"]&
							]
				|>;
		]


