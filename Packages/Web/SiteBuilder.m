(*TableOfContents*)



WebSiteNewTableOfContents[dir_String?DirectoryQ]:=
	Module[
		{
			outdir,
			metas,
			data,
			cells,
			postCounter=1
			},
		outdir=
			FileNameJoin@{dir, "posts"};
		metas=
			SortBy[
				AssociationMap[
					Association@
					Map[
						Rule@@StringTrim@StringSplit[#, ":",2]&,
						StringSplit[
							StringSplit[Import[#,"Text"],"\n\n",2][[1]],
							"\n"
							]
						]&,
					Select[
						FileNames["*.md",outdir,\[Infinity]],
						!StringMatchQ[FileBaseName[#], "toc"]&
						]
					],
				If[KeyMemberQ[#, "ID"],
					ToExpression[
						StringSplit[#ID, "."]
						],
					{1000,1000,1000}
					]&
				];
		data=
			GroupBy[First->Last]/@
				GroupBy[#[[1,1]]&->(#[[1,2]]->#[[2]]&)]@
					KeyValueMap[
							If[KeyMemberQ[#2, "Path"], 
								Take[URLParse[#2["Path"], "Path"], UpTo[2]],
								{}
								]->
							<|
								"Path"->
									URLBuild[
										Flatten@{
											"..",
											Most@FileNameSplit@StringTrim[#, outdir],
											#2["Slug"]<>".html"
											}
										],
								"Title"->
									If[KeyMemberQ[#2, "Title"], 
										#2["Title"],
										"Post #"<>ToString[postCounter++]
										]
								|>&,
						metas
						];
		cells=
			KeyValueMap[
				Cell[
					CellGroupData[Flatten@{
							Cell[#, "Section"],
							KeyValueMap[
								Cell[
									CellGroupData[
										Flatten@{
											Cell[#,"Subsection"],
											Map[
												Cell[
													TextData[
														ButtonBox[#Title,
															 BaseStyle->"Hyperlink",
															ButtonData->
															{
																FrontEnd`FileName[Evaluate@URLParse[#Path, "Path"]], 
																None
																}
															]
														],
													"Item"
													]&,
												#2
												]
											}
										]
									]&,
								#2
								]
							}
						]
					]&,
				data
				];
	WebSiteNewContent[dir,
		"pages",
		"toc",
		cells,
		{
			"Title"->"Table of Contents",
			"Slug"->"toc"
			}
		]
	]


