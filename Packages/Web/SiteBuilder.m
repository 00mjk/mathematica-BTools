(*webSiteDeploySelectFiles*)



webSiteDeploySelectFiles[
	dir_,
	last_,
	select_,
	fileForms_,
	alwaysDeployForms_,
	includes_
	]:=
	Select[
		FileExistsQ[#]&&
		!DirectoryQ[#]&&
			(
				StringMatchQ[
					#,
					alwaysDeployForms
					]||
				StringMatchQ[
					FileNameTake[#],
					alwaysDeployForms
					]||
				!DateObjectQ[last]||
				Quiet[FileDate[#]>last,Greater::nordol]
				)&&
			select[#]&
			]@
		SortBy[FileBaseName[#]==="index"&]@
		Join[
			Sequence@@
			Map[
				FileNames[
					fileForms,
					#,
					\[Infinity]]&,
				Select[Flatten@{dir, includes}, DirectoryQ]
				],
			Select[
				Flatten@List@includes, 
				Not@DirectoryQ[#]&&
					StringMatchQ[#,
						___~~fileForms
						]&
				]
			]


