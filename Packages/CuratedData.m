$CuratedDataPackageTemplate:=
	Import[
		`Package`appPath[
			"Packages",
			"__Templates__",
			"$CuratedData.m"],
		"Text"
		];


CuratedDataPaclet[
	dir:_String?DirectoryQ|Automatic:Automatic,
	dataType_String,
	entityStore_Association,
	pack:True|False:True,
	ops:(_String->_String)...
	]:=
	With[{
		file=
			With[{d=
				FileNameJoin@{
					Replace[dir,
						Automatic:>
							$TemporaryDirectory
						],
					dataType<>"Data"
					}
				},
				Quiet@
					CreateDirectory[
						FileNameJoin@{d,"AutoCompletionData"},
						CreateIntermediateDirectories->True
						];
				FileNameJoin@{
					d,
					dataType<>"Data.m"
					}
				]
		},
			(* --------- Package --------- *)
			With[{fob=OpenWrite@file},
				(WriteString[fob,#];Close@fob)&@
					StringReplace[$CuratedDataPackageTemplate,{
						ops,
						"$CuratedDataProperties"->
							ToString[
								DeleteCases["Label"]@
									Keys@entityStore["Properties"],
								InputForm
								],
						"$CuratedDataLabelFunction"->
							ToString[
								Replace[
									Lookup[
										Lookup[
											entityStore["Properties"],
											"Label",
											<||>
											],
										"DefaultFunction"
										],{
									_Missing|CommonName->CanonicalName
									}],
								InputForm
								],
						"$CuratedDataDefaultProperty"->
							ToString[
								Lookup[
									entityStore,
									"DefaultProperty",
									"Entity"
									]
							(*	First@DeleteCases["Label"]@
									Keys@entityStore["Properties"]*),
								InputForm
								],
						"$CuratedDataType"->dataType,
						"$CuratedData"->dataType<>"Data"
						}
					]
				];
				
			(* --------- Autocompletions --------- *)
			Export[
				FileNameJoin@{
					Replace[dir,
						Automatic:>
							$TemporaryDirectory
						],
					dataType<>"Data",
					"AutoCompletionData",
					"specialArgFunctions.tr"
					},
				StringReplace[
					Import[
						`Package`appPath[
							"Packages",
							"__Templates__",
							"$CuratedDataCompletions.tr"
							],
						"Text"
						],
					{
						"$CuratedDataEntities"->
							ToString[
								Function[
									DeleteDuplicates@
										Join[
											Map[First@StringSplit[#,"::"]&,#],
											#]
									]@
									SortBy[
										Keys@entityStore["Entities"],
										StringLength
										],
								InputForm
								],
						"$CuratedDataProperties"->
							ToString[
								SortBy[
									DeleteCases["Label"]@
										Keys@entityStore["Properties"],
									StringLength
									],
								InputForm
								],
						"$CuratedData"->dataType<>"Data"
						}],
				"Text"
				];
			If[pack,
				(PacletExpressionBundle[#];PacletBundle[#])&@
					FileNameJoin@{
						Replace[dir,
							Automatic:>
								$TemporaryDirectory
							],
						dataType<>"Data"
						},
				FileNameJoin@{
						Replace[dir,
							Automatic:>
								$TemporaryDirectory
							],
						dataType<>"Data"
						}
				]
		]


