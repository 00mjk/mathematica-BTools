dataPacletFunctionsIndexFile[
	dir_,
	dataType_String,
	entityStore_Association
	]:=
	With[{
		functions=
			With[{computeFunction=
				Symbol@Evaluate["DataPaclets`"<>dataType<>"DataDump`ComputeFunction"]
				},
			Normal@
				ReplacePart[#,
					"Helpers":>
						(
							Normal@GroupBy[First->Last]@Flatten@#["Helpers"]
							)
					]&@
			Join[
				<|
					"Primary"->{},
					"Helpers"->{}
					|>,
				GroupBy[First->Last]@
					Replace[
						Lookup[entityStore,"Functions",{}],{
							(Verbatim[HoldPattern][p___]:>f_):>
								"Primary"->
									(
										HoldPattern[computeFunction[p]]:>f
											),
							s_Symbol:>
								With[{key=
									SymbolName@Unevaluated@s
									},
									"Helpers"->
										{
											"OwnValues"->
												(key->OwnValues[s]),
											"DownValues"->
												(key->DownValues[s]),
											"UpValues"->
												(key->UpValues[s]),
											"SubValues"->
												(key->SubValues[s])
											}
									]
						}]
				]
			]
		},
		Export[
			FileNameJoin@{
				Replace[dir,
					Automatic:>
						$TemporaryDirectory
					],
				dataType<>"Data_Index",
				"Data",
				"Functions.wdx"
				},
			Compress@functions
			];
		]


