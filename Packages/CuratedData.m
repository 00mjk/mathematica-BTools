dataPacletDataFile[
	dir_,
	dataType_String,
	entityStore_Association
	]:=
	With[{
		ents=
			{
				"Keys"->
					Keys@entityStore["Entities"],
				"Properties"->
					Thread@List@
						DeleteCases["Label"]@
							Keys@entityStore["Properties"],
				"Data"->
					Map[
						Replace[
							Lookup[#,
								DeleteCases["Label"]@
									Keys@entityStore["Properties"]
								],
							_Missing->Missing["NotAvailable"],
							1]&,
						entityStore["Entities"]
						],
				"Attributes"->
					{
						"CreationDate"->DateList[],
						"Signature"->228610809693471781814095222429607185306
						}
				}
		},
		Export[
			FileNameJoin@{
				Replace[dir,
					Automatic:>
						$TemporaryDirectory
					],
				dataType<>"Data_Part01",
				"Data",
				"Part01.wdx"
				},
			ents,
			"DataTable"
			];
		];


CuratedDataIndexPaclet[
	dir:_String?DirectoryQ|Automatic:Automatic,
	dataType_String,
	entityStore_Association,
	pack:True|False:True
	]:=
	CompoundExpression[
		Begin["DataPaclets`CuratedDataFormattingDump`"],
		(End[];#)&@
			CompoundExpression[
				(* ------- Prep Directories  ------- *)
				Quiet@
					CreateDirectory[
						FileNameJoin@{
							Replace[dir,	
								Automatic:>
									$TemporaryDirectory
								],
							dataType<>"Data_Index",
							"Data"
							},
						CreateIntermediateDirectories->True
						];
				Quiet@
					CreateDirectory[
						FileNameJoin@{
							Replace[dir,	
								Automatic:>
									$TemporaryDirectory
								],
							dataType<>"Data_Part01",
							"Data"
							},
						CreateIntermediateDirectories->True
						],
				(* ------- Indices -------*)
				dataPacletIndexFile[dir,dataType,entityStore];
				dataPacletNamesIndexFile[dir,dataType,entityStore];
				dataPacletEntitiesIndexFile[dir,dataType,entityStore];
				dataPacletPropertiesIndexFile[dir,dataType,entityStore];
				dataPacletFunctionsIndexFile[dir,dataType,entityStore];
				dataPacletGroupsIndexFile[dir,dataType,entityStore];
				dataPacletPrivateGroupsIndexFile[dir,dataType,entityStore],
				(* ------- Data ------- *)
				dataPacletDataFile[dir,dataType,entityStore],
				(* ------- Paclets  ------- *)
				If[pack,
					Quiet@
						PacletExpressionBundle[
							FileNameJoin@{
								Replace[dir,
									Automatic:>
										$TemporaryDirectory
									],
								dataType<>"Data_Index"
								}
							];
					Quiet@
						PacletExpressionBundle[
							FileNameJoin@{
								Replace[dir,
									Automatic:>
										$TemporaryDirectory
									],
								dataType<>"Data_Part01"
								}
							];
					AssociationMap[
						PacletBundle@
							FileNameJoin@{
								Replace[dir,
									Automatic:>
										$TemporaryDirectory
									],
								dataType<>"Data_"<>#
								}&,
						{
							"Index",
							"Part01"
							}],
					<|
						"Index"->
							FileNameJoin@{
								Replace[dir,
									Automatic:>
										$TemporaryDirectory
									],
								dataType<>"Data_Index"
								},
						"Data"->
							FileNameJoin@{
								Replace[dir,
									Automatic:>
										$TemporaryDirectory
									],
								dataType<>"Data_Part01"
								}
						|>
					]
				]
		];


