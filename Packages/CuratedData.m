(*IndexPaclet*)



dataPacletIndexFile[
	dir:_String?DirectoryQ|Automatic:Automatic,
	dataType_String,
	entityStore_Association
	]:=
	With[{
		index=
			{
				"Sources"->
					{
						"Data"->
							{
							"Part01"->
								Keys@
									entityStore["Entities"]
									}
						},
				"Properties"->
					{
						"Data"->
							Thread@List@
								DeleteCases["Label"]@
									Keys@entityStore["Properties"]
						}
				}
		},
		Export[
			FileNameJoin@{
				Replace[dir,
					Automatic:>
						$TemporaryDirectory
					],
				dataType<>"Data_Index",
				"Data",
				"Index.wdx"
				},
			index,
			"DataIndex"
			]
		];


dataPacletNamesIndexFile[
	dir_,
	dataType_String,
	entityStore_Association
	]:=
	With[{
		names=
			Normal@
				AssociationMap[
					{#,First@StringSplit[#,"::"]}&,
					Flatten@{
						Keys@entityStore["Entities"],
						Lookup[
							Values@entityStore["Entities"],
							"AlternateNames",
							{}
							]
						}
					]
		},
		Export[
			FileNameJoin@{
				Replace[dir,
					Automatic:>
						$TemporaryDirectory
					],
				dataType<>"Data_Index",
				"Data",
				"Names.wdx"
				},
			names
			]
		];


